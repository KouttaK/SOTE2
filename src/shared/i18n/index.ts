let currentLanguage = 'pt-BR';

export function setLanguage(lang: string) {
  currentLanguage = lang;
}

/** Returns the language currently in effect (defaults to 'pt-BR'). */
export function getLanguage(): string {
  return currentLanguage;
}

export async function initI18n(): Promise<void> {
  // Language is set by settings; default to pt-BR
  try {
    const { storage } = await import('../storage/StorageService.js');
    const settings = await storage.getSettings();
    if (settings?.language) {
      currentLanguage = settings.language;
    }
  } catch {
    // Fallback silently
  }
}

export function t(key: string, variables?: Record<string, string | number>): string {
  const lang = (translations as any)[currentLanguage] ? currentLanguage : 'en';
  let str = (translations as any)[lang][key];
  // Fallback: try en, then return the key itself
  if (str === undefined) str = (translations as any)['en'][key];
  if (str === undefined) str = key;
  if (variables) {
    for (const [k, v] of Object.entries(variables)) {
      str = str.replace(`{${k}}`, v.toString());
    }
  }
  return str;
}
